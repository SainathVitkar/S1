package Java_Other_Programs;

public class Factorial2 
{

	public static void main(String[] args) 
	{
		int a,fact=1;  
		  int number=5;//It is the number to calculate factorial    
		  for(a=1;a<=number;a++)
		  {    
		      fact=fact*a;    
		  }    
		  System.out.println("Factorial of "+number+" is: "+fact);    
		 }  

	}
