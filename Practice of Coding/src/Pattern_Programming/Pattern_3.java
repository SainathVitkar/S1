package Pattern_Programming;

public class Pattern_3 
{

	public static void main(String[] args) 
	{
		for(int a=1;a<=5;a++) //12345
		{
			for(int b=1;b<=5;b++) //12345
			{
				System.out.print(b); //printing the 12345 for b variable
			}
			System.out.println(); //1st for loop getting execution without using the a variable
		}

	}

}
